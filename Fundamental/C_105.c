#include <stdio.h>

int main(){
    int a[4] = {10, 20, 30, 40};
    int b[5] = {50, 60, 70, 80, 90};
    int *p[2];

    p[0] = a; // p[0]에 a배열의 첫 요소를 저장.
    p[1] = b; // p[1]에 b배열의 첫 요소를 저장.

    printf("*(p[0]) = %d \n", *(p[0])); // 10
    printf("*(p[0] + 1) = %d \n", *(p[0] + 1)); // 20
    printf("*(p[1]) = %d \n", *p[1]); // 50
    printf("*(p[1]) + 15 = %d \n", *p[1] + 15); // 65

    return 0;
}